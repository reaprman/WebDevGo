package main

import (
	"bufio"
	"fmt"
	"io"
	"log"
	"net"
)

func main() {
	li, err := net.Listen("tcp", ":8080")
	if err != nil {
		log.Fatalln(err)
	}
	defer li.Close()

	for {
		conn, err := li.Accept()
		if err != nil {
			log.Fatalln(err)
		}
		go serve(conn)
	}
}

func serve(conn net.Conn) {
	defer conn.Close()
	scanner := bufio.NewScanner(conn)
	for scanner.Scan() {
		ln := scanner.Text()
		fmt.Println(ln)
		if ln == "" {
			// This is end of HEADER: empty line
			fmt.Println("This is END of the HTTP REQUEST HEADERS")
			break
		}

		// below line only works in firefox as chrome doesn't accept tcp connections
		// for design reasons chosen by google
		io.WriteString(conn, "Writing to the response.")
	}
}

